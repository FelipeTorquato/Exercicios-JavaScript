/*
Crie uma função que recebe dois parâmetros, base e expoente,
e retorne a base elevada ao expoente
*/

function exponenciacao(a, b) {
    return Math.pow(a, b)
}

console.log(exponenciacao(2, 1))
console.log(exponenciacao(2, 2))
console.log(exponenciacao(2, 3))
console.log(exponenciacao(2, 4))
console.log(exponenciacao(37, 3))